    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a delivery to confirm.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int deliveryId = (int) jTable1.getValueAt(selectedRow, 0);
        String deliveryDetails = jTextArea1.getText().trim();

        if (deliveryDetails.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter delivery details.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        transportDriverController.confirmDelivery(deliveryId, deliveryDetails);
        JOptionPane.showMessageDialog(this, "Delivery confirmed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

        populateDeliveryList();
        jTextArea1.setText("");


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

